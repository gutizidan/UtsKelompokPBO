    private void btnMasukanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMasukanActionPerformed
        // TODO add your handling code here:
        String Nn, Np;
        double Tp;
        int M = 0;
        if (optMember.isSelected()) {
            M = 1;
        } else if (optNonMember.isSelected()) {
            M = 2;
        }
        Nn = txNomorNota.getText();
        Np = txNamaPelanggan.getText();
        Tp = Double.parseDouble(txTotalPanjang.getText());
        InputLaporan(Nn, Np, Tp, M);
        txNomorNota.setText("");
        txNamaPelanggan.setText("");
        txTotalPanjang.setText("");
        JOptionPane.showMessageDialog(null, "Data Sukses Ditambah");
    }//GEN-LAST:event_btnMasukanActionPerformed

