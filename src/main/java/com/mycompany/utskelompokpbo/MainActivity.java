    private void InputLaporan(String nn, String np, double tp, int m) {
        model barang = new model();
        barang.setNomorNota(nn);
        barang.setNamaPelanggan(np);
        barang.setTotalPanjang(tp);
        barang.setMember(m);
        if (m == 1) {
            barang.setHarga(17000);
        } else {
            barang.setHarga(13000);
        }
        lst.add(barang);
    }
    private static ArrayList<model> lst;

    private void datatotable() {
        DefaultTableModel JTable1 = (DefaultTableModel) jTable1.getModel();
        JTable1.getDataVector().removeAllElements();
        JTable1.fireTableDataChanged();
        System.out.println("aaaaaaa" + lst.size());
        for (int i = 0; i < lst.size(); i++) {
            double total = lst.get(i).getTotalPanjang() * lst.get(i).getHarga();
            JTable1.addRow(new Object[]{lst.get(i).getNomorNota(), lst.get(i).getNamaPelanggan(), lst.get(i).getTotalPanjang(), lst.get(i).getHarga(), total});
        }

    }

